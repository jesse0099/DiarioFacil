    public void initInventario(){
        model  = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row,int column){
                return false;
            }
        };
        
        model.addColumn("Codigo");
        model.addColumn("Nombre");
        model.addColumn("Precio");
        model.addColumn("Existencias");
        model.addColumn("Existencia Min");
        //Llena la tabla con constantes
        for(Producto prod: DiarioFacilTester.diarioFacil.getProductos()){
              model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProductos().indexOf(prod)), prod.getNombre(),Double.toString(prod.getPrecio()),Integer.toString(prod.getExistencias()), Integer.toString(prod.getExistenciaMin())});
          }
        tableProductos.setModel(model);
    }
    
    public void initPromocion(){
        model  = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row,int column){
                return false;
            }
        };
        
        model.addColumn("Codigo");
        model.addColumn("Nombre");
        model.addColumn("Precio Promocional");
        model.addColumn("Estado");
        model.addColumn("Cantidad de Activación");
        model.addColumn("Fecha vencimiento");
        
        SimpleDateFormat formatter  = new SimpleDateFormat("yyyy-MM-dd hh:mm");
        //Llena la tabla con constantes
        for(Promocion prom: DiarioFacilTester.diarioFacil.getPromociones()){
          model.addRow(new Object[]{String.valueOf(DiarioFacilTester.diarioFacil.getPromociones().indexOf(prom)),  prom.getNombre(), Double.toString(prom.getPrecioPromocional()), Boolean.toString(prom.isEstado()), Integer.toString(prom.getCantidadActivacion()),formatter.format(prom.getFechaVencimiento())});
        }
        tablePromociones.setModel(model);
    }
