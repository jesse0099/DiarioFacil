    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        if(tablePromociones.getRowCount()>0){
            if(tablePromociones.getSelectedRow()>-1){
                if(txtPrecioPromo.getText().isEmpty() || txtCantidad.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Todos los campos son requeridos", "Sys", JOptionPane.ERROR_MESSAGE, new ImageIcon("src/edu/ulatina/ejemplos/img/icons8-error-32.png"));
                }else{
                    //Editar
                    boolean selected=false;
                    if(chbEstado.isSelected())
                        selected = true;
                    
                    if(DiarioFacilTester.diarioFacil.edit(Integer.parseInt(tablePromociones.getValueAt(tablePromociones.getSelectedRow(), 0).toString())  , new Promocion(Double.parseDouble(txtPrecioPromo.getText()), selected, Integer.parseInt(txtCantidad.getText()),txtNombre.getText(),Integer.parseInt(txtCodigo.getText()), Double.parseDouble(txtPrecio.getText()), Integer.parseInt(txtExistencias.getText()), Integer.parseInt(txtExistenciaMin.getText()),dateFechaV.getDate()))){
                        initPromocion();
                    }else{
                        JOptionPane.showMessageDialog(null, "Campos con informacion repetida", "Sys", JOptionPane.ERROR_MESSAGE, new ImageIcon("src/edu/ulatina/ejemplos/img/icons8-error-32.png"));
                    }
                }
            }
        }
        Clear();
        
        
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(tablePromociones.getRowCount()>0){
            if(tablePromociones.getSelectedRow()>-1){
                int respuesta  = JOptionPane.showConfirmDialog(null, "¿Seguro que desea eliminar este producto?", "Sys", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, new ImageIcon("src/edu/ulatina/ejemplos/img/icons8-error-32.png"));
                if(respuesta  == JOptionPane.YES_OPTION){
                    DiarioFacilTester.diarioFacil.delPromocion(Integer.parseInt(tablePromociones.getValueAt(tablePromociones.getSelectedRow(), 0).toString()));
                    initPromocion();
                }
            }
        }
        Clear();    
    }//GEN-LAST:event_btnDeleteActionPerformed

