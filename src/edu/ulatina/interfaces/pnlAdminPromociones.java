    private void tableProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductosMouseClicked
        //Muestra los datos de la fila seleccionada
        if(tableProductos.getRowCount()>0)
        if(tableProductos.getSelectedRow()>-1){
            txtCodigo.setText(String.valueOf(tableProductos.getValueAt(tableProductos.getSelectedRow(), 0)));
            txtNombre.setText(String.valueOf(tableProductos.getValueAt(tableProductos.getSelectedRow(), 1)));
            txtPrecio.setText(String.valueOf(tableProductos.getValueAt(tableProductos.getSelectedRow(), 2)));
            txtExistencias.setText(String.valueOf(tableProductos.getValueAt(tableProductos.getSelectedRow(), 3)));
            txtExistenciaMin.setText(String.valueOf(tableProductos.getValueAt(tableProductos.getSelectedRow(), 4)));
        }
   
    }//GEN-LAST:event_tableProductosMouseClicked

    private void tablePromocionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePromocionesMouseClicked
        //Muestra los datos de la fila seleccionada
        if(tablePromociones.getRowCount()>0)
        if(tablePromociones.getSelectedRow()>-1){
            txtCodigo.setText(String.valueOf(tablePromociones.getValueAt(tablePromociones.getSelectedRow(), 0)));
            txtNombre.setText(String.valueOf(tablePromociones.getValueAt(tablePromociones.getSelectedRow(), 1)));
            txtPrecioPromo.setText(String.valueOf(tablePromociones.getValueAt(tablePromociones.getSelectedRow(), 2)));
            txtCantidad.setText(String.valueOf(tablePromociones.getValueAt(tablePromociones.getSelectedRow(), 4)));
            txtExistencias.setText(String.valueOf(DiarioFacilTester.diarioFacil.getPromoExistencias(txtNombre.getText())));
            txtExistenciaMin.setText(String.valueOf(DiarioFacilTester.diarioFacil.getPromoExistenciaMinima(txtNombre.getText())));
            boolean estado = Boolean.parseBoolean(String.valueOf(tablePromociones.getValueAt(tablePromociones.getSelectedRow(), 3)));
            txtPrecio.setText(String.valueOf(DiarioFacilTester.diarioFacil.getPromoPrecio(txtNombre.getText())));
            dateFechaV.setDate(DiarioFacilTester.diarioFacil.getPromoDate(txtNombre.getText()));
            chbEstado.setSelected(estado);
        }
    }//GEN-LAST:event_tablePromocionesMouseClicked


