    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if(tableProductos.getRowCount()>0){
            if(tableProductos.getSelectedRow()>-1){
                if(txtNombre.getText().isEmpty() || txtPrecio.getText().isEmpty() || txtExistencias.getText().isEmpty()|| txtExistenciaMin.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Todos los campos son requeridos", "Sys", JOptionPane.ERROR_MESSAGE, new ImageIcon("src/edu/ulatina/ejemplos/img/icons8-error-32.png"));
                }else{
                    //Editar
                    if(DiarioFacilTester.diarioFacil.edit(tableProductos.getSelectedRow(), new Producto(txtNombre.getText(),DiarioFacilTester.diarioFacil.getProducos().size()+1 , Double.parseDouble(txtPrecio.getText()), Integer.parseInt(txtExistencias.getText()), Integer.parseInt(txtExistenciaMin.getText())))){
                        initInventario();
                    }else{
                        JOptionPane.showMessageDialog(null, "Campos con informacion repetida", "Sys", JOptionPane.ERROR_MESSAGE, new ImageIcon("src/edu/ulatina/ejemplos/img/icons8-error-32.png"));
                    }
                }
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        //Agrega una fila a la tabla
        model.insertRow(model.getRowCount(), new Object[] {tableProductos.getRowCount(), 1+1,txtCodigo.getText(), txtNombre.getText(), txtPrecio.getText(), txtExistencias.getText(), txtExistenciaMin.getText()});

        //Agrega un producto con los datos que hay en la tabla
        Producto producto = new Producto(txtNombre.getText(), DiarioFacilTester.diarioFacil.getProducos().size()+1, Double.parseDouble(txtPrecio.getText()), Integer.parseInt(txtExistencias.getText()), Integer.parseInt(txtExistenciaMin.getText()));
        DiarioFacilTester.diarioFacil.addProducto(producto);

        //Limpia los espacios
        Clear();
    }//GEN-LAST:event_btnAddActionPerformed

