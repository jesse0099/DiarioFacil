    private void rSMTextFull1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rSMTextFull1KeyPressed
        model  = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row,int column){
                return false;
            }
        };
        model.addColumn("Codigo");
        model.addColumn("Nombre");
        model.addColumn("Precio");
        model.addColumn("Existencias");
        model.addColumn("Existencia Mínima");
        if(cbFiltros.getSelectedItem().toString().equals("Codigo")){      
            
            for(Object prod : DiarioFacilTester.diarioFacil.read("codigo", rSMTextFull1.getText(), DiarioFacilTester.diarioFacil.getProducos())){
              model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProducos().indexOf((Producto)prod)), ((Producto)prod).getNombre(),Integer.toString(((Producto)prod).getCodigo()),Double.toString(((Producto)prod).getPrecio()),Integer.toString(((Producto)prod).getExistencias()),Integer.toString(((Producto)prod).getExistenciaMin())});
            }
            tableProductos.setModel(model);
        }else if (cbFiltros.getSelectedItem().toString().equals("Existencias")){
            for(Object prod : DiarioFacilTester.diarioFacil.read("exstencias", rSMTextFull1.getText(), DiarioFacilTester.diarioFacil.getProducos())){
              model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProducos().indexOf((Producto)prod)), ((Producto)prod).getNombre(),Integer.toString(((Producto)prod).getCodigo()),Double.toString(((Producto)prod).getPrecio()),Integer.toString(((Producto)prod).getExistencias()),Integer.toString(((Producto)prod).getExistenciaMin())});
            }
            tableProductos.setModel(model);
        }else if(cbFiltros.getSelectedItem().toString().equals("Precio")){
            for(Object prod : DiarioFacilTester.diarioFacil.read("precio", rSMTextFull1.getText(), DiarioFacilTester.diarioFacil.getProducos())){
              model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProducos().indexOf((Producto)prod)), ((Producto)prod).getNombre(),Integer.toString(((Producto)prod).getCodigo()),Double.toString(((Producto)prod).getPrecio()),Integer.toString(((Producto)prod).getExistencias()),Integer.toString(((Producto)prod).getExistenciaMin())});
            }
            tableProductos.setModel(model); 
        }else{
            for(Object prod : DiarioFacilTester.diarioFacil.read("nombre", rSMTextFull1.getText(), DiarioFacilTester.diarioFacil.getProducos())){
              model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProducos().indexOf((Producto)prod)), ((Producto)prod).getNombre(),Integer.toString(((Producto)prod).getCodigo()),Double.toString(((Producto)prod).getPrecio()),Integer.toString(((Producto)prod).getExistencias()),Integer.toString(((Producto)prod).getExistenciaMin())});
            }
            tableProductos.setModel(model); 
        }
    }//GEN-LAST:event_rSMTextFull1KeyPressed


