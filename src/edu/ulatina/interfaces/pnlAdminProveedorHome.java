    public void initProveedores(){
        model  = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row,int column){
                return false;
            }
        };
        model.addColumn("Codigo");
        model.addColumn("Nombre");
        model.addColumn("Apellido");
        model.addColumn("Cedula");
        model.addColumn("Empresa");
        model.addColumn("Email");
        model.addColumn("Numero contacto");
        for(Proveedor prov : DiarioFacilTester.diarioFacil.getProveedores()){
          model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProveedores().indexOf(prov)), prov.getNombre(),prov.getApellido(),prov.getCedula(),prov.getEmpresa(),prov.getEmail(),prov.getNumeroContacto()});
        }
        jtbProveedores.setModel(model);
    }

