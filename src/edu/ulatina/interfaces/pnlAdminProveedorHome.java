          else{
            //Agregar 
            if(DiarioFacilTester.diarioFacil.create(new Proveedor(txtEmpresa.getText(),txtNumeroContacto.getText(),txtCedula.getText(),txtNombre.getText(),txtApellido.getText(),tXtCorreo.getText()))){
                //Mensaje de agregado correctamente
                initProveedores();
            }else{
                //Mensaje de error
                 JOptionPane.showMessageDialog(null, "Campos con informacion repetida", "Sys", JOptionPane.ERROR_MESSAGE, new ImageIcon("src/edu/ulatina/ejemplos/img/icons8-error-32.png"));
            }
          }
      //</editor-fold>
