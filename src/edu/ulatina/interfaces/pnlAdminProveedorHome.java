    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(jtbProveedores.getRowCount()>0){
            if(jtbProveedores.getSelectedRow()>-1){
                int respuesta  = JOptionPane.showConfirmDialog(null, "Seguro desea eliminar este proveedor?", "Sys", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, new ImageIcon("src/edu/ulatina/ejemplos/img/icons8-error-32.png"));
                if(respuesta  == JOptionPane.YES_OPTION){
                    //Consultar si el proveedor tiene productos registrados en el futuro jaja
                    DiarioFacilTester.diarioFacil.deleteProveedor(jtbProveedores.getSelectedRow());
                    initProveedores();
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void rSMTextFull1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rSMTextFull1KeyTyped
        // TODO add your handling code here:
        model  = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row,int column){
                return false;
            }
        };
        model.addColumn("Codigo");
        model.addColumn("Nombre");
        model.addColumn("Apellido");
        model.addColumn("Cedula");
        model.addColumn("Empresa");
        model.addColumn("Email");
        model.addColumn("Numero contacto");
        if(cbFiltros.getSelectedItem().toString().equals("Empresa")){      
            for(Object prov : DiarioFacilTester.diarioFacil.read("empresa", rSMTextFull1.getText(), DiarioFacilTester.diarioFacil.getProveedores())){
              model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProveedores().indexOf((Proveedor)prov)), ((Proveedor)prov).getNombre(),((Proveedor)prov).getApellido(),((Proveedor)prov).getCedula(),((Proveedor)prov).getEmpresa(),((Proveedor)prov).getEmail(),((Proveedor)prov).getNumeroContacto()});
            }
            jtbProveedores.setModel(model);
        }else if (cbFiltros.getSelectedItem().toString().equals("Numero de contacto")){
            for(Object prov : DiarioFacilTester.diarioFacil.read("numeroContacto", rSMTextFull1.getText(), DiarioFacilTester.diarioFacil.getProveedores())){
              model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProveedores().indexOf((Proveedor)prov)), ((Proveedor)prov).getNombre(),((Proveedor)prov).getApellido(),((Proveedor)prov).getCedula(),((Proveedor)prov).getEmpresa(),((Proveedor)prov).getEmail(),((Proveedor)prov).getNumeroContacto()});
            }
            jtbProveedores.setModel(model);
        }else if(cbFiltros.getSelectedItem().toString().equals("Producto")){
            for(Object prov : DiarioFacilTester.diarioFacil.read("productos", rSMTextFull1.getText(), DiarioFacilTester.diarioFacil.getProveedores())){
              model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProveedores().indexOf((Proveedor)prov)), ((Proveedor)prov).getNombre(),((Proveedor)prov).getApellido(),((Proveedor)prov).getCedula(),((Proveedor)prov).getEmpresa(),((Proveedor)prov).getEmail(),((Proveedor)prov).getNumeroContacto()});
            }
            jtbProveedores.setModel(model); 
        }else{
            for(Object prov : DiarioFacilTester.diarioFacil.read("nombre", rSMTextFull1.getText(), DiarioFacilTester.diarioFacil.getProveedores())){
              model.addRow(new String[]{String.valueOf(DiarioFacilTester.diarioFacil.getProveedores().indexOf((Proveedor)prov)), ((Proveedor)prov).getNombre(),((Proveedor)prov).getApellido(),((Proveedor)prov).getCedula(),((Proveedor)prov).getEmpresa(),((Proveedor)prov).getEmail(),((Proveedor)prov).getNumeroContacto()});
            }
            jtbProveedores.setModel(model); 
        }

    }//GEN-LAST:event_rSMTextFull1KeyTyped


