    private Double calcSubTotal(){
       Double returned =0.00;
       for(int i=0;i<tblCarrito.getRowCount();i++){
           returned = returned+ Double.parseDouble(tblCarrito.getValueAt(i,5).toString());
       }
       return returned;
    }
    
